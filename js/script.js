let baloesRestantes = 0;
const urlParams = new URLSearchParams(window.location.search);
const slug = urlParams.get('nome')?.toLowerCase();
const dados = dadosMulheres[slug];

if (dados) {
    document.title = "Para " + dados.nome;
    document.getElementById('boas-vindas').innerText = "Surpresa para " + dados.nome + "!";
    document.getElementById('main-container').style.backgroundImage = `url('${dados.background}')`;
    
    const caixaElemento = document.querySelector('.caixa-dialogo');
    if (dados.imagemCaixa) {
        caixaElemento.style.backgroundImage = `url('${dados.imagemCaixa}')`;
    }

    const player = document.getElementById('musica-fundo');
    const source = document.getElementById('audio-source');
    source.src = dados.musica;
    player.load();

    if (dados.pularMusica) player.currentTime = dados.pularMusica;

    if (dados.personagemOverlay) {
        const niko = document.createElement('img');
        niko.src = dados.personagemOverlay;
        niko.id = "niko-img";
        document.getElementById('overlay').appendChild(niko);
    }

    const ativarSom = () => { player.play().catch(e => {}); document.removeEventListener('click', ativarSom); };
    document.addEventListener('click', ativarSom);

    document.getElementById('texto-instrucao').innerText = dados.instrucao;
    document.body.classList.add(dados.classeTema);
    baloesRestantes = dados.pontos.length;
} else {
    document.body.innerHTML = "<div class='text-center mt-5'><h1 class='text-white'>Página não encontrada.</h1></div>";
}

function iniciarExperiencia() {
    const overlay = document.getElementById('overlay');
    overlay.style.opacity = '0';
    setTimeout(() => { overlay.style.display = 'none'; }, 500);

    document.querySelector('.container-instrucao').style.display = 'block';
    document.getElementById('hotspots-container').style.display = 'block';
    
    document.getElementById('musica-fundo').play();
    renderizarPontos();
}

function renderizarPontos() {
    const container = document.getElementById('hotspots-container');
    container.innerHTML = "";
    dados.pontos.forEach(ponto => {
        const wrapper = document.createElement('div');
        wrapper.className = 'hotspot';
        wrapper.style.left = ponto.x;
        wrapper.style.top = ponto.y;

        if (dados.classeTema === "tema-bea") {
            const monkey = document.createElement('div');
            monkey.className = 'monkey-img';
            const balloon = document.createElement('div');
            balloon.className = 'balloon-img';
            balloon.onclick = (e) => { e.stopPropagation(); balloon.classList.add('popped'); baloesRestantes--; abrirMensagem(ponto.msg); };
            wrapper.appendChild(monkey); wrapper.appendChild(balloon);
        } else if (dados.classeTema === "tema-cirsa") {
            const bible = document.createElement('div');
            bible.className = 'bible-dot';
            bible.onclick = () => { bible.classList.add('popped'); baloesRestantes--; abrirMensagem(ponto.msg); };
            wrapper.appendChild(bible);
        } else if (dados.classeTema === "tema-islane") {
            const africa = document.createElement('div');
            africa.className = 'africa-dot';
            africa.onclick = () => { africa.classList.add('popped'); baloesRestantes--; abrirMensagem(ponto.msg); };
            wrapper.appendChild(africa);
        } else if (dados.classeTema === "tema-irani") {
            const pan = document.createElement('div');
            pan.className = 'pan-dot';
            pan.onclick = () => { pan.classList.add('popped'); baloesRestantes--; abrirMensagem(ponto.msg); };
            wrapper.appendChild(pan);
        } else {
            const dot = document.createElement('div');
            dot.className = 'default-dot';
            dot.onclick = () => { dot.classList.add('popped'); baloesRestantes--; abrirMensagem(ponto.msg); };
            wrapper.appendChild(dot);
        }
        container.appendChild(wrapper);
    });
}

function abrirMensagem(texto) {
    setTimeout(() => {
        const textoModal = document.getElementById('texto-mensagem');
        textoModal.innerText = texto;
        const modal = new bootstrap.Modal(document.getElementById('mensagemModal'));
        modal.show();
        const modalElement = document.getElementById('mensagemModal');
        const checkFinal = function () {
            if (baloesRestantes === 0) exibirMensagemFinal();
            modalElement.removeEventListener('hidden.bs.modal', checkFinal);
        };
        modalElement.addEventListener('hidden.bs.modal', checkFinal);
    }, 400);
}

function exibirMensagemFinal() {
    setTimeout(() => {
        const textoFinal = document.getElementById('texto-final');
        textoFinal.innerText = dados.mensagemFinal;
        const modalFinal = new bootstrap.Modal(document.getElementById('modalFinal'));
        modalFinal.show();
    }, 600);
}